package RayTracerObj;

public class Point {
	final private double x;
	final private double y;
	final private double z;
	
	/**
	 * This class represent Point in Cartesian coordinates
	 * @param x
	 * @param y
	 * @param z
	 */
	public Point(double x, double y, double z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
}
